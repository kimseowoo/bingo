#include <stdio.h>
#include <stdlib.h>

/* run this program using the console pauser or add your own getch, system("pause") or input loop */

int main(void) {

	int N;
	int i,j;
	int s[i][j];
	
	N=5;
	
	for(i=0;i<N-1;i++)
	{
		for(j=0;j<N-1;j++)
		{
				printf("%d",s[i][j]=rand()%100);
				printf("\n");
		}
	}
	
	
	return 0;
}
	

